package in.prepskool.prepskoolacademy.retrofit_model;

public class User {
    private String id;
    private String name;
    private String email;
    private String role;
    private String phone;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getRole() {
        return role;
    }

    public String getPhone() {
        return phone;
    }
}
